  } else {
    Write-Host "  ★ $(T '全部完成！' 'All done!')" -ForegroundColor Green
  }
  Write-Host "  $(T '厂商' 'Provider')   $($p.label) ($Model)"
  Write-Host "  model      $ModelId"
  Write-Host ""
  Write-Host "  $(T '现在怎么用：' 'How to start:')" -ForegroundColor Cyan
  Write-Host "    1. $(T '进入你的项目文件夹：' 'Go into your project folder:') cd <your-folder>"
  Write-Host "    2. $(T '输入命令启动：' 'Launch:') claude" -ForegroundColor Green
  Note (T "如果提示找不到 claude，关掉 PowerShell 重开一个再试" "If 'claude' is not found, reopen PowerShell")
