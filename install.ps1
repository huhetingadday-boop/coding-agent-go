function Select-Provider {
  Write-Host ""
  Write-Host "  $(T '第一步：选择要用的模型（输入数字后回车）' 'Step 1: choose a model (type a number, Enter)')" -ForegroundColor White
  Write-Host "    1) 智谱 GLM        $(T '推荐 · 编程最强' 'recommended · best at coding')" -ForegroundColor Cyan
  Write-Host "    2) Kimi (Moonshot) $(T '长上下文' 'long context')" -ForegroundColor Cyan
  Write-Host "    3) MiniMax         $(T '便宜' 'cheap')" -ForegroundColor Cyan
  Write-Host "    4) 通义千问 Qwen   $(T '阿里' 'Alibaba')" -ForegroundColor Cyan
  Write-Host "    5) DeepSeek        $(T '推理强' 'strong reasoning')" -ForegroundColor Cyan
  $c = Read-Host (T "  你的选择 [1]" "  Your choice [1]")
