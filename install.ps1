<#
  yiclaude — Windows installer: Claude Code + a domestic Chinese model.
  Native PowerShell entry point (use this on Windows; install.sh is for macOS/Linux).

  Beginner mode (no arguments) = a guided wizard:
    1. pick a model from a menu
    2. if you have an API key, paste it; if not, it opens the page and waits
    3. it installs Git + Claude Code and configures everything for you

  One command (run in PowerShell):
    irm https://cdn.jsdelivr.net/gh/your-name/yiclaude@main/install.ps1 | iex

  With flags:
    & ([scriptblock]::Create((irm https://cdn.jsdelivr.net/gh/your-name/yiclaude@main/install.ps1))) -Model glm -ApiKey sk-xxx

  Notes:
    - Targets Windows 10 (1809+) and Windows 11. Installs whatever is missing.
    - Claude Code on Windows runs commands through Git Bash, so Git is required.
    - Tries the official source first, then a China mirror as fallback.
#>
