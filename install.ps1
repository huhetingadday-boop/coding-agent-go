function Acquire-Key {
  $p = $Providers[$Model]
  $ans = Read-Host (T "  第二步：你有 $($p.label) 的 API key 吗？(y/N)" "  Step 2: do you have a $($p.label) API key? (y/N)")
  if ($ans -notmatch '^(y|yes)$') {
    if (-not $NoOpen) { try { Start-Process $p.key } catch {} }
    Write-Host ""
    Write-Host "  $(T '没关系，照下面 4 步走，1 分钟就能拿到：' 'No problem — 4 steps, about 1 minute:')" -ForegroundColor Yellow
    Write-Host "    1  $(T "打开 $($p.label) 的 API key 页面（已自动帮你打开浏览器；没弹出就手动复制下面链接）" "Open the $($p.label) API key page (auto-opened; if not, copy the link)")" -ForegroundColor Green
    Write-Host "       $($p.key)" -ForegroundColor Cyan
    Write-Host "    2  $(T '注册或登录账号（部分厂商需要先实名认证）' 'Register or sign in (some need ID verification first)')" -ForegroundColor Green
    Write-Host "    3  $(T '点“创建 / 新建 API Key”，复制那串 key' "Click 'Create API Key' and copy the key")" -ForegroundColor Green
    Write-Host "    4  $(T '回到这个窗口，粘贴进来按回车' 'Come back here, paste it, press Enter')" -ForegroundColor Green
    Write-Host "    $(T '看不懂可参考官方图文文档：' 'Official docs:') $($p.doc)" -ForegroundColor DarkGray
  }
  while ($true) {
    $script:ApiKey = Read-Host (T "  把 API key 粘贴到这里，然后回车" "  Paste your API key here, then Enter")
    if ([string]::IsNullOrWhiteSpace($script:ApiKey)) {
      Warn (T "还没收到 key，再粘贴一次（或按 Ctrl+C 退出）" "no key yet — paste again (or Ctrl+C to quit)")
      continue
    }
    if (Test-ApiKeyLooksPlausible $script:ApiKey) { break }
    Warn (T "这看起来不像 API key。请只粘贴厂商控制台生成的那串 key。" "that does not look like an API key. Paste only the key generated by the provider console.")
    $script:ApiKey = ""
  }
  Ok (T "已收到 API key" "API key received")
}

# ---- prerequisites: install whatever is missing ---------------------------
function Install-GitFromMirror {
  # Fallback when winget is unavailable or failed: download the official
  # Git for Windows installer from a China mirror and run it silently.
  Note (T "改用国内镜像下载 Git 安装包…" "Falling back to a China mirror for Git…")
  $base = "https://mirrors.huaweicloud.com/git-for-windows"
  $idx  = Invoke-WebRequest -Uri "$base/" -UseBasicParsing -TimeoutSec 30
  $ver  = ($idx.Links.href | Where-Object { $_ -match '^v?\d+\.\d+' } |
           Sort-Object -Descending | Select-Object -First 1).TrimEnd('/')
  $page = Invoke-WebRequest -Uri "$base/$ver/" -UseBasicParsing -TimeoutSec 30
  $file = ($page.Links.href | Where-Object { $_ -match 'Git-.*-64-bit\.exe$' } | Select-Object -First 1)
  if (-not $file) { throw "no installer found on mirror" }
  $out  = Join-Path $env:TEMP $file
  Invoke-WebRequest -Uri "$base/$ver/$file" -OutFile $out -UseBasicParsing -TimeoutSec 600
  Start-Process -FilePath $out -ArgumentList "/VERYSILENT","/NORESTART","/NOCANCEL" -Wait
}

function Ensure-Git {
  if (Have-Cmd "git") { Ok (T "Git 已安装" "Git already installed"); return }
  Warn (T "未发现 Git，正在安装（Claude Code 在 Windows 上靠它跑命令）" "Git not found; installing (Claude Code needs it on Windows)")
  if (Have-Cmd "winget") {
    try { winget install --id Git.Git -e --accept-source-agreements --accept-package-agreements | Out-Null } catch {}
    Refresh-Path
    if (Have-Cmd "git") { Ok (T "Git 安装完成（官方/winget）" "Git installed (winget)"); return }
  }
  try { Install-GitFromMirror; Refresh-Path } catch {}
  if (Have-Cmd "git") { Ok (T "Git 安装完成（国内镜像）" "Git installed (mirror)"); return }
  Die (T "Git 自动安装失败。请手动装 Git for Windows 后重试：https://git-scm.com/download/win" "Auto-install of Git failed. Install Git for Windows manually, then retry: https://git-scm.com/download/win")
}

function Step-Prereq {
  Begin-Step (T "检查并安装前置依赖" "Check & install prerequisites")
  Ok (T "系统: Windows" "OS: Windows")
  if (-not (Have-Cmd "winget")) {
    Warn (T "未发现 winget（应用安装程序）；会尽量用国内镜像直接装。建议从 Microsoft Store 更新“应用安装程序”以获得最佳体验。" "winget (App Installer) not found; will use China mirrors directly. Updating 'App Installer' from Microsoft Store is recommended.")
  }
  Ensure-Git
}

# ---- install Claude Code (official first, then winget) ---------------------
function Step-InstallClaude {
  Begin-Step (T "安装 Claude Code" "Install Claude Code")
  if (Have-Cmd "claude") { Ok (T "Claude Code 已安装" "Claude Code already installed"); return }
  Note (T "先试官方源，失败再用 winget" "Trying official source first, then winget")
  $done = $false
  try {
    Invoke-RestMethod -Uri "https://claude.ai/install.ps1" -TimeoutSec 20 | Invoke-Expression
    Refresh-Path
    if (Have-Cmd "claude") { $done = $true }
  } catch {}
  if (-not $done -and (Have-Cmd "winget")) {
    try { winget install --id Anthropic.ClaudeCode -e --accept-source-agreements --accept-package-agreements | Out-Null } catch {}
    Refresh-Path
    if (Have-Cmd "claude") { $done = $true }
  }
  if (-not $done) {
    Die (T "Claude Code 安装失败，请看上面的输出，或关掉 PowerShell 重开再试" "Claude Code install failed; check output or reopen PowerShell")
  }
  Ok (T "Claude Code 安装完成" "Claude Code installed")
}

# ---- configure model: safe-merge into settings.json -----------------------
# We merge our env keys into any existing ~/.claude/settings.json (preserving
# the user's other fields) using PowerShell's native JSON — no python/jq needed.
# The result is a clean settings.json that cc-switch can later import.
function Step-Configure {
  $p = $Providers[$Model]
  Begin-Step (T "配置模型：$($p.label)" "Configure model: $($p.label)")
  if (-not $BaseUrl)   { $script:BaseUrl   = $p.base }
  if (-not $ModelId)   { $script:ModelId   = $p.model }
  if (-not $FastModel) { $script:FastModel = $p.fast }

  $dir = Join-Path $env:USERPROFILE ".claude"
  New-Item -ItemType Directory -Force -Path $dir | Out-Null
  $path = Join-Path $dir "settings.json"

  $data = $null
  if (Test-Path $path) {
    Copy-Item $path "$path.bak" -Force
    try { $data = Get-Content $path -Raw | ConvertFrom-Json } catch { $data = $null }
  }
  if ($null -eq $data) { $data = [pscustomobject]@{} }

  $envObj = $null
  if ($data.PSObject.Properties.Name -contains 'env') { $envObj = $data.env }
  if ($null -eq $envObj) { $envObj = [pscustomobject]@{} }

  $envObj | Add-Member -NotePropertyName 'ANTHROPIC_BASE_URL'         -NotePropertyValue $BaseUrl   -Force
  $envObj | Add-Member -NotePropertyName 'ANTHROPIC_AUTH_TOKEN'       -NotePropertyValue $ApiKey    -Force
  $envObj | Add-Member -NotePropertyName 'ANTHROPIC_MODEL'            -NotePropertyValue $ModelId   -Force
  $envObj | Add-Member -NotePropertyName 'ANTHROPIC_SMALL_FAST_MODEL' -NotePropertyValue $FastModel -Force
  $data | Add-Member -NotePropertyName 'env' -NotePropertyValue $envObj -Force

  # Write UTF-8 WITHOUT BOM (Claude Code's JSON parser rejects a leading BOM).
  $json = $data | ConvertTo-Json -Depth 12
  [System.IO.File]::WriteAllText($path, $json, (New-Object System.Text.UTF8Encoding($false)))

  Ok "$(T '已写入配置：' 'Config written: ')$path"
  Note "base_url = $BaseUrl"
  Note "model    = $ModelId"
}

# ---- verify connectivity ---------------------------------------------------
function Step-Verify {
  if ($NoVerify) { return }
  Begin-Step (T "验证连通性" "Verify connectivity")
  $p = $Providers[$Model]
  $headers = @{ "content-type"="application/json"; "anthropic-version"="2023-06-01"; "authorization"="Bearer $ApiKey" }
  $payload = @{ model=$ModelId; max_tokens=8; messages=@(@{ role="user"; content="ping" }) } | ConvertTo-Json -Depth 6
  $code = 0
  try {
    $resp = Invoke-WebRequest -Uri "$($BaseUrl.TrimEnd('/'))/v1/messages" -Method Post -Headers $headers -Body $payload -TimeoutSec 40 -UseBasicParsing
    $code = [int]$resp.StatusCode
  } catch {
    if ($_.Exception.Response) { $code = [int]$_.Exception.Response.StatusCode.value__ }
  }
  if ($code -eq 200) { Ok (T "连接成功，模型可用！" "Connected — the model works!"); return }
  Warn (T "验证失败 (HTTP $code)。常见原因：" "Verify failed (HTTP $code). Common causes:")
  switch ($code) {
    { $_ -in 401,403 } { Write-Host "  $(T '→ API key 不对或没权限' '→ wrong/unauthorized API key')" }
    404                { Write-Host "  $(T '→ 接口地址不对，或该厂商路径有变' '→ wrong base URL or path changed')" }
    { $_ -in 400,422 } { Write-Host "  $(T '→ 模型名可能过期，试试 -ModelId <新模型名>' '→ model id may be stale — try -ModelId <id>')" }
    default            { Write-Host "  $(T '→ 网络不通或超时' '→ network error or timeout')" }
  }
  Write-Host "  $(T '厂商文档：' 'Provider docs: ')$($p.doc)"
  Warn (T "配置已写入，但模型暂不可用。修正后重跑即可。" "Config written but model not usable yet. Fix and re-run.")
  $script:Failed = $true
}

