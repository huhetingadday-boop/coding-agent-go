function Test-ApiKeyLooksPlausible([string]$Candidate) {
  if ([string]::IsNullOrWhiteSpace($Candidate)) { return $false }
  if ($Candidate.Length -lt 16) { return $false }
  if ($Candidate -match '\s|<|>|API key|api key|your |你的|创建|新建|复制|粘贴|generated') { return $false }
  return $true
}

function Assert-ApiKeyLooksPlausible {
  if (Test-ApiKeyLooksPlausible $ApiKey) { return }
  Die (T "这不像有效 API key。请粘贴厂商控制台生成的完整 key，不要粘贴说明文字或占位符。" "this does not look like a valid API key. Paste the full key generated by the provider console, not instructions or placeholders.")
}

